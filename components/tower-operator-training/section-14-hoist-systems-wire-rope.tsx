      <p>
        Steel rope conducts electricity—contact with energized sources can energize rope and structure. In specialized work near lines or
        substations, <strong>synthetic</strong> lines (e.g. Dyneema / UHMWPE—often blue) reduce conduction risk but do not replace MAD,
        isolation, or electrical safe work procedures—moisture, contamination, or metal fittings can still conduct.
      </p>
      <p>
